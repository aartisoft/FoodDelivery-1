package com.haris.meal4u.ObjectUtil;

public class StepperObject {

    private String title;
    private String description;
    private int icon;


    public String getTitle() {
        return title;
    }

    public StepperObject setTitle(String title) {
        this.title = title;
        return this;
    }

    public String getDescription() {
        return description;
    }

    public StepperObject setDescription(String description) {
        this.description = description;
        return this;
    }

    public int getIcon() {
        return icon;
    }

    public StepperObject setIcon(int icon) {
        this.icon = icon;
        return this;
    }
}
